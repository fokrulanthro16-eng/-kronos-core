"""
PDF export service.

Generates branded KRONOS CORE PDFs using reportlab.
All functions accept a plain dict and return raw PDF bytes.
No network calls, no secrets, no side effects.
"""
from __future__ import annotations

import io
from datetime import datetime, timezone
from typing import Any

import structlog

log = structlog.get_logger()

# ── Colours (hex strings) ─────────────────────────────────────────────────────
_GREEN  = "#00cc55"   # slightly dimmed brand green — readable on white
_DARK   = "#0d1117"
_GRAY   = "#444444"
_LGRAY  = "#888888"
_WHITE  = "#ffffff"


def _rl():
    """Lazy import of reportlab — raises ImportError with clear message if missing."""
    try:
        import reportlab  # noqa: F401
        return True
    except ImportError as exc:
        raise ImportError(
            "reportlab is not installed. Run: pip install reportlab"
        ) from exc


def _make_doc(buf: io.BytesIO):
    from reportlab.lib.pagesizes import A4
    from reportlab.platypus import SimpleDocTemplate
    from reportlab.lib.units import mm

    return SimpleDocTemplate(
        buf,
        pagesize=A4,
        rightMargin=22 * mm,
        leftMargin=22 * mm,
        topMargin=22 * mm,
        bottomMargin=22 * mm,
    )


def _styles():
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.colors import HexColor
    from reportlab.lib.enums import TA_CENTER

    base = getSampleStyleSheet()
    GREEN = HexColor(_GREEN)
    DARK  = HexColor(_DARK)
    GRAY  = HexColor(_GRAY)
    LGRAY = HexColor(_LGRAY)

    return {
        "title": ParagraphStyle(
            "KTitle", parent=base["Normal"],
            textColor=DARK, fontSize=22, leading=28,
            fontName="Helvetica-Bold", spaceAfter=2,
        ),
        "subtitle": ParagraphStyle(
            "KSubtitle", parent=base["Normal"],
            textColor=GREEN, fontSize=13, leading=16,
            fontName="Helvetica-Bold", spaceAfter=8,
        ),
        "h2": ParagraphStyle(
            "KH2", parent=base["Normal"],
            textColor=GREEN, fontSize=10, leading=14,
            fontName="Helvetica-Bold", spaceBefore=10, spaceAfter=4,
        ),
        "body": ParagraphStyle(
            "KBody", parent=base["Normal"],
            textColor=GRAY, fontSize=9, leading=14, spaceAfter=2,
        ),
        "bullet": ParagraphStyle(
            "KBullet", parent=base["Normal"],
            textColor=GRAY, fontSize=9, leading=13,
            leftIndent=12, spaceAfter=2,
        ),
        "small": ParagraphStyle(
            "KSmall", parent=base["Normal"],
            textColor=LGRAY, fontSize=7.5, leading=11,
        ),
        "footer": ParagraphStyle(
            "KFooter", parent=base["Normal"],
            textColor=LGRAY, fontSize=7.5, leading=10,
            alignment=TA_CENTER,
        ),
        "meta": ParagraphStyle(
            "KMeta", parent=base["Normal"],
            textColor=LGRAY, fontSize=8, leading=12, spaceAfter=2,
        ),
        "badge": ParagraphStyle(
            "KBadge", parent=base["Normal"],
            textColor=HexColor(_GREEN), fontSize=8,
            fontName="Helvetica-Bold", leading=12, spaceAfter=4,
        ),
    }


def _hr(color=_GREEN, thickness=1.5):
    from reportlab.platypus import HRFlowable
    from reportlab.lib.colors import HexColor
    return HRFlowable(width="100%", thickness=thickness, color=HexColor(color), spaceAfter=4)


def _sp(mm_val=4):
    from reportlab.platypus import Spacer
    from reportlab.lib.units import mm
    return Spacer(1, mm_val * mm)


def _bullets(items: list, style, prefix="•") -> list:
    from reportlab.platypus import Paragraph
    return [Paragraph(f"{prefix}  {item}", style) for item in items if item]


def _section(title: str, story: list, styles: dict):
    from reportlab.platypus import Paragraph
    story.append(Paragraph(title, styles["h2"]))
    story.append(_hr(color=_GREEN, thickness=0.5))


def _footer_text() -> str:
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    return f"Generated by KRONOS CORE — Autonomous Security & Prompt Architecture Gateway · {ts} · CONFIDENTIAL"


# ── Enterprise Report PDF ─────────────────────────────────────────────────────

def build_enterprise_report_pdf(report_data: dict) -> bytes:
    _rl()
    from reportlab.platypus import Paragraph

    buf = io.BytesIO()
    doc = _make_doc(buf)
    s   = _styles()

    story: list[Any] = []

    # Header block
    story.append(Paragraph("KRONOS CORE", s["title"]))
    story.append(Paragraph("Enterprise Security Report", s["subtitle"]))
    story.append(_hr())
    story.append(_sp(4))

    # Metadata
    story.append(Paragraph(f"Product: {report_data.get('product', 'KRONOS CORE')}", s["meta"]))
    story.append(Paragraph(f"Report ID: {report_data.get('report_id', 'N/A')}", s["meta"]))
    story.append(Paragraph(f"Generated: {report_data.get('generated_at', 'N/A')}", s["meta"]))
    story.append(Paragraph("★  ENTERPRISE READY", s["badge"]))
    story.append(_sp(3))

    # Executive Summary
    _section("Executive Summary", story, s)
    story.append(Paragraph(report_data.get("executive_summary", ""), s["body"]))
    story.append(_sp(3))

    # Capabilities
    _section("Capabilities", story, s)
    story.extend(_bullets(report_data.get("capabilities", []), s["bullet"]))
    story.append(_sp(3))

    # Compliance
    _section("Compliance Alignment", story, s)
    story.extend(_bullets(report_data.get("compliance_alignment", []), s["bullet"]))
    story.append(_sp(3))

    # Integration + Deployment
    _section("Integration Options", story, s)
    story.extend(_bullets(report_data.get("integration_options", []), s["bullet"]))
    story.append(_sp(3))

    _section("Deployment Models", story, s)
    story.extend(_bullets(report_data.get("deployment_models", []), s["bullet"]))
    story.append(_sp(3))

    # Pricing
    _section("Pricing Model", story, s)
    story.append(Paragraph(report_data.get("pricing_model", ""), s["body"]))
    story.append(_sp(3))

    # Support
    _section("Support Model", story, s)
    story.append(Paragraph(report_data.get("support_model", ""), s["body"]))
    story.append(_sp(3))

    # References
    _section("References & Standards", story, s)
    story.extend(_bullets(report_data.get("references", []), s["small"]))
    story.append(_sp(8))

    # Footer
    story.append(_hr(color=_LGRAY, thickness=0.5))
    story.append(Paragraph(_footer_text(), s["footer"]))

    doc.build(story)
    log.info("pdf_built", report_type="enterprise", size_bytes=buf.tell())
    return buf.getvalue()


# ── Blueprint Report PDF ──────────────────────────────────────────────────────

