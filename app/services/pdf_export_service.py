    pkg = report_data.get("package_policy", {})
    if pkg:
        _section("Package Policy — Allowed", story, s)
        story.extend(_bullets(pkg.get("allowed", []), s["bullet"], prefix="✓"))
        story.append(_sp(2))
        _section("Package Policy — Forbidden", story, s)
        story.extend(_bullets(pkg.get("forbidden", []), s["bullet"], prefix="✗"))
        story.append(_sp(3))

    _section("Deployment Checklist", story, s)
    story.extend(_bullets(report_data.get("deployment_checklist", []), s["bullet"]))
    story.append(_sp(3))

    _section("Production Readiness Checklist", story, s)
    story.extend(_bullets(report_data.get("production_readiness_checklist", []), s["bullet"]))
